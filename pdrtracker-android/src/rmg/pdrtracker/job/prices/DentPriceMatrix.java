    public String getDentPrice(DentDamageKey dentDamageKey) {
        CarArea carArea = dentDamageKey.getCarArea();
        DamageClassifier damageClassifier = dentDamageKey.getDamageClassifier();
        DentSize dentSize = dentDamageKey.getDentSize();
        int priceInt = prices[carArea.ordinal()][damageClassifier.ordinal()][dentSize.ordinal()];
        return priceInt == -1 ? "CR" : Integer.toString(priceInt);
    }

    public String setDentPrice(DentDamageKey dentDamageKey, int priceInt) {
        CarArea carArea = dentDamageKey.getCarArea();
        DamageClassifier damageClassifier = dentDamageKey.getDamageClassifier();
        DentSize dentSize = dentDamageKey.getDentSize();
        prices[carArea.ordinal()][damageClassifier.ordinal()][dentSize.ordinal()] = priceInt;
        return priceInt == -1 ? "CR" : Integer.toString(priceInt);
    }

